import string as s

print(f'Все буквы английского алфавита: {s.ascii_letters}')
print(f'Маленькие буквы английского алфавита: {s.ascii_lowercase}')
print(f'Большие буквы английского алфавита: {s.ascii_uppercase}')
print(f'Цифры: {s.digits}')
print(f'HEX-алфавит: {s.hexdigits}')
print(f'Спец.символы: {s.punctuation}')


